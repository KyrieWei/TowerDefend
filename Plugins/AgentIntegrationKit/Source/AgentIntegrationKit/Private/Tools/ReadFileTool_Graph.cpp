static FString GetPinDefaultValueString(const UEdGraphPin* Pin)
{
	if (!Pin)
	{
		return TEXT("");
	}

	// Prefer explicit object defaults.
	if (Pin->DefaultObject)
	{
		return Pin->DefaultObject->GetPathName();
	}

	// Then text defaults, then scalar/string defaults.
	if (!Pin->DefaultTextValue.IsEmpty())
	{
		return Pin->DefaultTextValue.ToString();
	}

	if (!Pin->DefaultValue.IsEmpty())
	{
		return Pin->DefaultValue;
	}

	// Fall back to autogenerated defaults so agents can still inspect values
	// on unlinked pins (e.g., bool false, enum default selection).
	if (!Pin->AutogeneratedDefaultValue.IsEmpty())
	{
		return Pin->AutogeneratedDefaultValue;
	}

	return TEXT("");
}

static FString BuildPinDefaultsSummary(UEdGraphNode* Node)
{
	if (!Node)
	{
		return TEXT("");
	}

	TArray<FString> PinDefaults;

	for (UEdGraphPin* Pin : Node->Pins)
	{
		if (!Pin)
		{
			continue;
		}

		// Defaults are meaningful for unlinked input pins.
		if (Pin->Direction != EGPD_Input || Pin->LinkedTo.Num() > 0)
		{
			continue;
		}

		// Exec/default-implicit pins are not useful in this section.
		if (Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Exec)
		{
			continue;
		}

		FString DefaultValue = GetPinDefaultValueString(Pin).TrimStartAndEnd();
		if (DefaultValue.IsEmpty())
		{
			continue;
		}

		// Hidden pins are often implicit engine wiring; only include if an explicit
		// default was authored (not just autogenerated fallback noise).
		const bool bHasExplicitDefault =
			Pin->DefaultObject != nullptr ||
			!Pin->DefaultTextValue.IsEmpty() ||
			!Pin->DefaultValue.IsEmpty();
		if (Pin->bHidden && !bHasExplicitDefault)
		{
			continue;
		}

		// Skip noisy implicit object/class "None" defaults.
		const bool bObjectLike =
			Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Object ||
			Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_Class ||
			Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_SoftObject ||
			Pin->PinType.PinCategory == UEdGraphSchema_K2::PC_SoftClass;
		if (bObjectLike && DefaultValue.Equals(TEXT("None"), ESearchCase::IgnoreCase))
		{
			continue;
		}

		// Keep node output one-line and tab-safe.
		DefaultValue.ReplaceInline(TEXT("\r"), TEXT(" "));
		DefaultValue.ReplaceInline(TEXT("\n"), TEXT(" "));
		DefaultValue.ReplaceInline(TEXT("\t"), TEXT(" "));

		PinDefaults.Add(FString::Printf(TEXT("%s=%s"), *Pin->PinName.ToString(), *DefaultValue));
	}

	if (PinDefaults.Num() == 0)
	{
		return TEXT("");
	}

	FString Summary = FString::Join(PinDefaults, TEXT("; "));
	constexpr int32 MaxLen = 400;
	if (Summary.Len() > MaxLen)
	{
		Summary = Summary.Left(MaxLen) + TEXT("...");
	}

	return Summary;
}
